            <h3 style="font-style: italic; font-weight: bold;">Directions</h3>
            <p style="margin-bottom: 10px;">
            <ul>
            <li>firstly, in a large vessel take 8 cup water, 2 tbsp oil, 1 tsp salt.</li>
            <br>
            <li>once the water comes to a boil, add 2 pack noodles.</li>
            <br>
            <li>stir and boil for 3 minutes or until the noodles turn al dente. refer package instructions to know the cooking time.</li>
            <br>
            <li>drain off the noodles and rinse with cold water to stop the cooking process. keep aside.</li>
            <br>
            <li>in a large wok heat 2 tbsp oil and add 2 clove garlic, 1 chilli, 2 inch ginger and 2 tbsp spring onion.</li>
            <br>
            <li>stir fry on high flame.</li>
            <br>
            <li>now add ½ onion, ½ capsicum, ½ cabbage, 1 carrot, 2 beans. stir fry until the vegetables shrink slightly.</li>
            <br>
            <li>further add 2 tbsp chilli sauce, 2 tbsp soy sauce, 2 tbsp tomato sauce, ½ tsp salt and ½ tsp pepper powder.</li>
            <br>
            <li>stir fry until the sauces well combine.</li>
            <br>
            <li>now add boiled noodles, 2 tbsp vinegar and stir fry.</li>
            <br>
            <li>also add few bean sprouts and 2 tbsp spring onions. stir fry.</li>
            <br>
            <li>finally, enjoy veg hakka noodles garnished with more spring onions.</li>
