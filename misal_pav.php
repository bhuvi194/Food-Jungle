            <h3 style="font-style: italic; font-weight: bold;">Directions</h3>
            <p style="margin-bottom: 10px;">
            <ul>
            <li>firstly, in a large kadai heat 3 tbsp oil and splutter 1 tsp mustard, 1 tsp jeera and few curry leaves.</li>
            <br>
            <li>also add ¼ tsp turmeric, 1 tsp chilli powder, 1 tsp coriander powder and 1 tsp garam masala.</li>
            <br>
            <li>saute on low flame till spices turn aromatic.</li>
            <br>
            <li>now add in prepared masala paste and saute well.</li>
            <br>
            <li>cook till the oil is released from masala paste.</li>
            <br>
            <li>add in cooked matki, small piece jaggery and ½ tsp salt. give a good mix.</li>
            <br>
            <li>also add 5 cup water and adjust consistency.</li>
            <br>
            <li>cover and boil for 10 minutes or till misal is cooked completely.</li>
            <br>
            <li>once the misal is cooked, oil starts to float indicating misal is ready.</li>
            <br>
            <li>in a serving plate, take matki usal add some farsan over it.</li>
            <br>
            <li>top with chopped onions and coriander leaves over it.</li>
            <br>
            <li>also pour ladleful of kat or gravy from sides.</li>
            <br>
            <li>finally, serve misal with pav and lemon wedges making a complete misal pav recipe.</li>
