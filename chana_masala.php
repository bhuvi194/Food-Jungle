            <h3 style="font-style: italic; font-weight: bold;">Directions</h3>
            <p style="margin-bottom: 10px;">
            <ul>
                <h4>Pressure cooking chana: </h4>
            <li>firstly, in a large bowl take 1 cup chickpea and soak in enough water for 8 hours.</li>
            <br>
            <li>transfer the soaked chickpea to a pressure cooker.</li>
            <br>
            <li>add ¼ tsp baking soda, ¾ tsp salt and 3 cup water.</li>
            <br>
            <li>pressure cook for 5 to 6 whistles or until the chana is cooked well.</li>
            <br>
                <h4>Chana masala powder preparation: </h4>
            <li>firstly, in a pan take ¼ cup coriander seeds, ¼ cup cumin, 2 tsp pepper, 1 tsp clove, 2 pod black cardamom, 2 inch cinnamon, 1 mace, 1 tsp fennel, 1 tsp shah jeera.</li>
            <br>
            <li>roast on low flame until the spices turn aromatic.</li>
            <br>
            <li>further add 10 dried red chilli and roast until the chilli puffs up and turns crunchy.</li>
            <br>
            <li>cool completely and transfer to a mixi.</li>
            <br>
            <li>add 2 tbsp aamchur, 2 tbsp kasuri methi and 1 tsp turmeric.</li>
            <br>
            <li>blend to fine powder without adding any water.</li>
            <br>
            <li>chole masala powder is ready. store in an airtight container for longer shelf life.</li>
            <br>
                 <h4>Chana Masala curry Preparation: </h4>
            <li>firstly, in a large kadai heat 2 tbsp ghee and saute 3 pod cardamom, 1 bay leaf, 1 tsp cumin until spices turn aromatic.</li>
            <br>
            <li>add 1 onion, 1 chilli, 1 tsp ginger garlic paste and saute until the onions turn golden brown.</li>
            <br>
            <li>further add in 1 tbsp of prepared chole masala and ½ tsp salt. saute on low flame until the masala turn aromatic.</li>
            <br>
            <li>now add 2 cup tomato puree and mix well. to prepare tomato puree, blend two ripened tomatoes to a smooth paste without adding any water.</li>
            <br>
            <li>saute until the oil separates from the masala paste.</li>
            <br>
            <li>now add in pressure cooked chana and mix well adjusting consistency as required.</li>
            <br>
            <li>cover and simmer for 10 minutes or until the flavours are well absorbed.</li>
            <br>
            <li>finally, enjoy 2 tbsp coriander and enjoy chana masala with roti, puri or hot steamed rice.</li>
            <br>
