            <h3 style="font-style: italic; font-weight: bold;">Directions</h3>
            <p style="margin-bottom: 10px;">
            <ul>
            <li>firstly, in a large bowl take ¾ cup milk powder, ½ cup maida and ½ tsp baking powder.</li>
            <br>
            <li>mix well, homemade gulab jamun mix is ready.</li>
            <br>
            <li>now add 2 tbsp ghee and mix well making the flour moist.</li>
            <br>
            <li>further, add milk as required start to combine.</li>
            <br>
            <li>combine well forming a soft dough. do not knead the dough.</li>
            <br>
            <li>cover and rest for 10 minutes.</li>
            <br>
            <li>meanwhile, prepare the sugar syrup by taking 2 cup sugar, 2 cup water, 2 cardamom and ¼ tsp saffron.</li>
            <br>
            <li>mix well and boil for 5 minutes or until the sugar syrup turns sticky. do not attain any string consistency.</li>
            <br>
            <li>turn off the flame and add 1 tsp lemon juice and 1 tsp rose water. lemon juice is added to prevent sugar syrup from crystallizing.</li>
            <br>
            <li>cover and keep the sugar syrup aside.</li>
            <br>
            <li>after 10 minutes or resting the dough, start to prepare small ball sized jamuns.</li>
            <br>
            <li>make sure there are no cracks in the jamun. if there are cracks then there are high chances for jamuns to break while frying.</li>
            <br>
            <li>deep fry in medium hot oil or ghee. frying in ghee gives good flavour to jamuns.</li>
             <br>
             <li>stir continuously and fry on low flame.</li>
             <br>
             <li>fry until the jamuns turn golden brown.</li>
             <br>
             <li>drain off and transfer the jamun into a hot sugar syrup.</li>
             <br>
             <li>cover and rest for 2 hours or until jamuns absorb the sugar syrup and doubles in size.</li>
             <br>
             <li>finally, enjoy gulab jamun with ice cream or as it is.</li>
            </ul>
