          <h3 style="font-style: italic; font-weight: bold;">Ingredients</h3>
          <p style="margin-bottom: 20px;">
             "3/4 cup of parboiled rice"
             <br>
             "3/4 cup of regular rice"
             <br>
             "1/2 cup Whole Urad Dal (without skin) or Split Urad Dal (black lentils)"
             <br>
             "1/4 teaspoon Fenugreek Seeds (methi dana)"
             <br>
             "1/2 tablespoon Chana Dal (gram lentils), optional"
             <br>
             "Salt"
             <br>
            </p>
            
            <h3 style="font-style: italic; font-weight: bold;">Directions</h3>
            <p style="margin-bottom: 10px;">
            <ul>
              <h4> Soaking rice and lentils: </h4>
            <li>In a bowl take ½ cup idli rice or parboiled rice along with ½ cup regular rice. Instead of adding regular rice, you can also make the dosa with a total of 1 cup idli rice as I have shown in the video. Kindly note that the video has the recipe ingredients doubled in proportion.</li>
            <br>
            <li> To the same bowl, add ¼ cup urad dal and ⅛ teaspoon fenugreek seeds (methi).</li>
            <br>
            <li>Rinse the rice, lentils and methi seeds together a couple of times and keep them aside.</li>
            <br>
            <li> In a separate bowl, take 2 tablespoons of thick poha (flattened rice or parched rice).</li>
            <br>
            <li>Rinse poha once or twice in water and then add to the bowl containing the rinsed rice+lentils+methi seeds.</li>
            <br>
            <li>Add 1.5 cups of water and mix. Cover with a lid and soak everything for 4 to 5 hours.</li>
            <br>
            <li>Drain all the water and add the soaked ingredients to a mixer grinder or a blender.</li>
            <br>
            <li>Add ⅔ to ¾ cup water and grind or blend till you get a fine grainy consistency of rice in the batter. A smooth consistency of the batter is also fine.</li>
            <br>
            <li>Now take the batter in a large bowl or pan. In case the dosa batter becomes thin, then add a few tablespoons of rice flour to thicken it. Mix the rice flour very well in the batter.</li>
            <br>
            <li>Add ½ teaspoon of edible rock salt. Mix very well. Instead of rock salt, you can use non-iodized salt or sea salt crystals or Himalayan pink salt.</li>
            <br>
               <h4> Fermenting Dosa Batter: </h4>
            <li> Cover and allow to ferment for 8 to 9 hours or more. The time of fermentation will vary depending on the temperature conditions, climate and altitude.</li>
            <br>
            <li>Now lightly stir the batter, before you begin to make dosa. You will also see many tiny air pockets in the batter. As you see in the below photo, the batter is very well fermented.</li>
            <br>
                <h4>Making Dosa: </h4>
            <li>Heat a cast iron pan. When the pan becomes hot, spread ¼ to ½ teaspoon oil all over the pan with a spoon or with a thick paper towel dipped in oil. For a richer taste, you can cook the dosa with ghee or butter.</li>
            <br>
            <li> Now take a ladle full of the dosa batter or about ¼ cup of the batter. Pour the batter and gently spread the batter starting from the center and moving outwards. In the below photo you will see a well-spread dosa batter.</li>
            <br>
            <li>Cook the dosa on low to medium heat. Do regulate the heat as per the pan size and thickness. You can even cover the dosa with a lid and let it get cooked from the bottom as I do.</li>
            <br>
            <li> Cook till the base is nicely golden and crisp. The base will leave the pan and the edges separate when it gets cooked.</li>
            <br>
            <li>When the dosa has nicely browned from the bottom and the surface looks well cooked, soft without any specks of undercooked batter, then fold with a spatula and lift it from the pan.</li>
            <br>
            <li>Serve Dosa hot with coconut chutney, potato masala and sambar. It is best to serve these crispy dosa hot.</li>
            <br>
