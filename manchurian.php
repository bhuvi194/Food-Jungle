            <h3 style="font-style: italic; font-weight: bold;">Directions</h3>
            <p style="margin-bottom: 10px;">
            <ul>
            <li>firstly, in a large bowl take 1 carrot, 5 tbsp cabbage, 2 tbsp spring onion, ½ onion and ½ tsp ginger garlic paste.</li>
            <br>
            <li>also add 1 tsp chilli sauce, ½ tsp salt.</li>
            <br>
            <li>mix well making sure everything is combined well.</li>
            <br>
            <li>further, add 2 tbsp maida, 2 tbsp cornflour and mix well.</li>
            <br>
            <li>form a soft dough adding maida if required.</li>
            <br>
            <li>now prepare a small ball sized balls.</li>
            <br>
            <li>deep fry in hot oil keeping the flame on medium.</li>
            <br>
            <li>stir occasionally, until the balls turn golden brown.</li>
            <br>
            <li>drain off the manchurian balls and keep aside.</li>
            <br>
            <li>now in a large wok, heat 2 tbsp oil and saute 1 inch ginger, 3 clove garlic, 1 chilli and 2 tbsp spring onion.</li>
            <br>
            <li>add ½ onion and stir fry on high flame.</li>
            <br>
            <li>further, add ½ capsicum and stir fry until it shrinks slightly.</li>
            <br>
            <li>add 2 tbsp vinegar, 2 tbsp soy sauce, 1 tsp chilli sauce, 2 tbsp tomato sauce, ½ tsp pepper powder and ¼ tsp salt.</li>
            <br>
            <li>stir fry making sure all the sauces are well combined.</li>
            <br>
            <li>add in 1¼ cup water and boil on medium flame.</li>
            <br>
            <li>in a small bowl take 2 tsp cornflour and ¼ cup water.</li>
            <br>
            <li>mix well forming a lump-free slurry.</li>
            <br>
            <li>pour in cornflour slurry and mix well.</li>
            <br>
            <li>continue to boil until the gravy thickens and turns glossy.</li>
            <br>
            <li>add in prepared veg manchurian balls, 2 tbsp spring onion and mix well.</li>
            <br>
            <li>finally, serve veg manchurian gravy with fried rice.</li>
            <br>
            </ul>
