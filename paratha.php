            <h3 style="font-style: italic; font-weight: bold;">Directions</h3>
            <p style="margin-bottom: 10px;">
            <ul>
            <li>Start with preparing the dough. In a mixing bowl add whole wheat flour, all-purpose flour, salt, and oil.</li>
            <br>
            <li>Add water to make a soft and pliable dough. Knead the dough with the palm of your hand for 2-3 minutes as that not only helps to release the glutton but makes a soft dough. When done, keep aside and prepare to make the masala.</li>
            <br>
            <li>Take a small bowl and add red chilli powder, salt to taste and carom seeds.</li>
            <br>
            <li>After that add coriander powder and pomegranate seeds green chillies and chopped coriander leaves. Mix them well with a spoon and then add half a teaspoon of ghee to the bowl. Mix the masala to make a thick paste and keep it aside.</li>
            <br>
            <li>After dividing the dough into equal parts, roll the dough in the palm of your hands to make balls. Dust the balls in flour and with a rolling pin make a circle.</li>
            <br>
            <li>Now, take the masala stuffing and gently spread it all over the paratha.</li>
            <br>
            <li>When the masala is evenly spread take a knife and make a slit from the middle to the edge. Make a straight cut from the centre towards the edge. When done, keep the knife aside and with your fingers, roll the cut to the other side.</li>
            <br>
            <li>When it achieves a vertical shape, press the head to the centre and fold it. Fold it well and start rolling the paratha. Do dust some flour first. With gentle movement roll the paratha out once again to about a 6-7 inch diameter circle.</li>
            <br>
            <li>Heat a griddle or tawa and smear it with a teaspoon of ghee. Now gently place the paratha over it. Keep pressing the paratha with a spoon or spatula for even cooking of paratha.</li>
            <br>
            <li>Keep the heat from medium to low and cook the paratha until it is crisp and golden. When the parathas are well-cooked turn off the heat and serve hot.</li>
            <br>
            </ul>
