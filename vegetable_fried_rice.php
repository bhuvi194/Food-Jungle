            <li>stir fry on high flame without overcooking vegetables.</li>
            <br>
            <li>now add 2 tbsp soy sauce and 1 tbsp vinegar. stir-fry until the sauce is combined well.</li>
            <br>
            <li>keeping the flame on high, add cooked rice.</li>
            <br>
            <li>also, add 1 tsp pepper and ¼ tsp salt. adjust the salt as soy sauce has salt.</li>
            <br>
